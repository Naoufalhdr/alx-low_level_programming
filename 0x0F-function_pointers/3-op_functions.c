#include "3-calc.h"

/**
 * op_add - add two integers
 * @a: first number
 * @b: second number
 *
 * Return: an integer wich is the sum of the two numbers.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract two integers
 * @a: first number
 * @b: second number
 *
 * Return: an integer wich is the difference of the two numbers.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two integers
 * @a: first number
 * @b: second number
 *
 * Return: an integer wich is the product of the two numbers.
 */
/* Function to multiply two integers */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two integers
 * @a: first number
 * @b: second number
 *
 * Return: an integer wich is the quotien division of the two numbers.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - add two integers
 * @a: first number
 * @b: second number
 *
 * Return: integer wich is the remainder obtained by dividing the two numbers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
